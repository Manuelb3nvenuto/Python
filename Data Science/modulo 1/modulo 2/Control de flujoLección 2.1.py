# Con elif (else if) para múltiples condiciones
nota = 85

if nota >= 90:
    print("Excelente! A+")
elif nota >= 80:
    print("Muy bien! B")
elif nota >= 70:
    print("Aprobado raspando C")
else:
    print("Reprobado... estudia más 💪")